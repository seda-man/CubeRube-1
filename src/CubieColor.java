public class CubieColor {

	private char color;
	private char dir;
	
	public CubieColor(char color, char dir) {
		this.color = color;
		this.dir = dir;
	}
	
	public char getColor() {
		return color;
	}
	
	public char getDir() {
		return dir;
	}
	
	public void setDir(char dir) {
		this.dir = dir;
	}
	
	public void setColor(char color) {
		this.color = color;
	}
	
}
